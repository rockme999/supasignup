/**
 * build-changelog.mjs
 *
 * docs/CHANGELOG.md (개발자용) 와 docs/PUBLIC_CHANGELOG.md (운영자용) 를
 * raw markdown string 으로 읽어 workers/api/src/data/changelog.ts 를 자동 생성한다.
 *
 * 실행 방법 (workers/api 폴더 기준):
 *   node ../../scripts/build-changelog.mjs
 *
 * 생성된 changelog.ts 는 .gitignore 에 등록되어 git 추적 대상이 아님.
 *
 * 이스케이프 처리:
 *   - 백틱(`) → \`  (template literal 종료 방지)
 *   - ${ → \${      (template literal 보간 방지)
 */

import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

// ── 경로 계산 ──────────────────────────────────────────────────
const repoRoot = resolve(__dirname, '..');
const internalChangelogPath = resolve(repoRoot, 'docs/CHANGELOG.md');
const publicChangelogPath = resolve(repoRoot, 'docs/PUBLIC_CHANGELOG.md');
const outDir = resolve(repoRoot, 'workers/api/src/data');
const outPath = resolve(outDir, 'changelog.ts');

// ── template literal 이스케이프 헬퍼 ─────────────────────────
function escapeForTemplateLiteral(raw) {
  return raw
    .replace(/\\/g, '\\\\')    // 백슬래시 먼저 이스케이프
    .replace(/`/g, '\\`')      // 백틱
    .replace(/\$\{/g, '\\${'); // 보간 시작
}

// ── CHANGELOG.md (개발자용) 읽기 ──────────────────────────────
let rawInternal = '';
try {
  rawInternal = readFileSync(internalChangelogPath, 'utf8');
} catch (err) {
  console.error(`[build-changelog] CHANGELOG.md 읽기 실패: ${err.message}`);
  process.exit(1);
}

// ── PUBLIC_CHANGELOG.md (운영자용) 읽기 ──────────────────────
let rawPublic = '';
try {
  rawPublic = readFileSync(publicChangelogPath, 'utf8');
  console.log(`[build-changelog] PUBLIC_CHANGELOG.md 읽기 성공: ${rawPublic.length} bytes`);
} catch (err) {
  console.warn(`[build-changelog] WARNING: PUBLIC_CHANGELOG.md 읽기 실패 — placeholder 사용: ${err.message}`);
  rawPublic = '# 번개가입 변경사항\n\n운영자용 변경사항이 아직 없습니다.\n';
}

// ── 이스케이프 ─────────────────────────────────────────────────
const escapedInternal = escapeForTemplateLiteral(rawInternal);
const escapedPublic = escapeForTemplateLiteral(rawPublic);

// ── 출력 ──────────────────────────────────────────────────────
mkdirSync(outDir, { recursive: true });

const content = `// AUTO-GENERATED by scripts/build-changelog.mjs — DO NOT EDIT
// CHANGELOG_PUBLIC  : docs/PUBLIC_CHANGELOG.md  (운영자용)
// CHANGELOG_INTERNAL: docs/CHANGELOG.md         (개발자용)

export const CHANGELOG_PUBLIC = \`${escapedPublic}\`;

export const CHANGELOG_INTERNAL = \`${escapedInternal}\`;

/** @deprecated CHANGELOG_MARKDOWN 은 CHANGELOG_INTERNAL 의 alias — 기존 코드 호환용 */
export const CHANGELOG_MARKDOWN = CHANGELOG_INTERNAL;
`;

writeFileSync(outPath, content, 'utf8');
console.log(`[build-changelog] 생성 완료: ${outPath}`);
console.log(`  PUBLIC  크기: ${rawPublic.length} bytes`);
console.log(`  INTERNAL 크기: ${rawInternal.length} bytes`);
